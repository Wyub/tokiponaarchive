<p><suml>Lesson 2</suml><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>Toki Pona has consonants jklmnpstw and vowels aeiou.</sumt><br>
  <sumn>2</sumn> <sumt>The consonants are pronounced the same as in most Western languages, except <tp>j</tp> is like English's letter <eng>y.</eng></sumt><br>
  <sumn>3</sumn> <sumt>The vowels are pronounced the same as in Esperanto, Italian, and Spanish.<br>

<p><suml>Lesson 3</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumn>Nouns</sumn> <sumv2>jan, mi, moku, sina, suno, telo</sumv2><br>
  <sumn>Verbs/Adjectives</sumn> <sumv2>moku, pona, suli</sumv2><br>
  <sumn>Grammatical words</sumn> <sumv2>li</sumv2><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>Toki Pona lacks the verb <eng>to be,</eng> is often ambiguous, and its verbs have no tense.</sumt><br>
  <sumn>2</sumn> <sumt>If the subject is <tp>mi</tp> or <tp>sina</tp>, procees from the subject straight to the predicate.</sumt><br>
  <sumn>3</sumn> <sumt>If the subject is anything other than <tp>mi</tp> or <tp>sina</tp>, then <tp>li</tp> must separate the subject from the predicate.</sumt>

<p><suml>Lesson 4</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumn>Nouns</sumn> <sumv2>ilo, kili, ni, ona, pipi, ma, ijo</sumv2><br>
  <sumn>Verbs/Adjectives</sumn> <sumv2>jo, lukin, pakala, unpa, wile</sumv2><br>
  <sumn>Grammatical words</sumn> <sumv2>e</sumv2><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt><tp>e</tp> must precede all direct objects.</sumt><br>
  <sumn>2</sumn> <sumt><eng>want to</eng> <eq></eq> <tp>wile</tp>.</sumt><br>
  <sumn>3</sumn> <sumt>You may stack <tp>li</tp> or <tp>e</tp> phrases within a sentence.</sumt>

<p><suml>Lesson 5</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>ike, jaki, lawa, len, lili, mute, nasa, seli, sewi, tomo, utala</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>Adjectives always follow the nouns they modify. This is true even for possessive pronouns.</sumt><br>
  <sumn>2</sumn> <sumt>Adverbs always follows the verbs they modify.</sumt><br>
  <sumn>3</sumn> <sumt>Toki Pona strings words together into phrases to make more complex meanings. These strings often act as nouns.</sumt>

<p><suml>Lesson 6</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>kama, kepeken, kiwen, kon, lon, pana, poki, toki, tawa</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>As a verb, <tp>lon</tp> essentially means <eng>to be in/on/at.</eng> As a preposition, it simply means, <eng>in/on/at.</eng></sumt><br>
  <sumn>2</sumn> <sumt>Unlike English, Toki Pona doesn't allow clauses. Use <tp>wile e ni: [...]</tp> instead.</sumt><br>
  <sumn>3</sumn> <sumt>As a verb, <tp>kepeken</tp> means <eng>to use.</eng> As a preposition, it means <eng>using.</eng></sumt><br>
  <sumn>4</sumn> <sumt><tp>tawa</tp> can be an action verb (<eng>to move</eng>), a verb of movement (<eng>to go to</eng>), or a preposition (<eng>to</eng> and <eng>for</eng>).</sumt><br>
  <sumn>5</sumn> <sumt>When followed by <tp>e</tp>, <tp>kama</tp> means <eng>to cause.</eng> When preceding another verb, it makes the verb progressive.</sumt>

<p><suml>Lesson 7</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>anpa, insa, monsi, poka, sama, sewi, tan</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>To express the locations <eng>below</eng> (<tp>anpa</tp>), <eng>inside</eng> (<tp>insa</tp>), <eng>behind</eng> (<tp>monsi</tp>),
<eng>beside</eng> or <eng>with</eng> (<tp>poka</tp>), and <eng>above</eng> (<tp>sewi</tp>), use the respective Toki Pona word after a preposition (usually <tp>lon</tp> or <tp>tawa</tp>).</sumt>
<p><suml>Lesson 8</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>ala, ale/ali, ken, lape, musi, pali, sona, wawa</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt><tp>ala</tp> immediately follows whatever word it negates.</sumt><br>
  <sumn>2</sumn> <sumt>"(verb) <tp>ala</tp> (repeat the verb)" makes yes/no questions.</sumt><br>
  <sumn>3</sumn> <sumt>To answer yes/no questions, "(verb)" <eq></eq> yes, "(verb) <tp>ala</tp>" <eq></eq> no.</sumt> 

<p><suml>Lesson 9</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>a, awen, mama, mije, meli, mu, nimi, o, pona, toki</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>Toki Pona does not have grammatical gender. Gender can be ascribed by adding mije or meli as adjectives.</sumt><br>
  <sumn>2</sumn> <sumt>Religions, countries, languages, names, etc. are unofficial words. They are treated like adjectives and must be attached to a noun.</sumt><br>
  <sumn>3</sumn> <sumt>To get someone's attention, say, "<tp>jan</tp> (person's name) <tp>o</tp>."</sumt><br>
  <sumn>4</sumn> <sumt>To issue a command, say, "<tp>o</tp> (verb)."</sumt><br>
&nbsp;&nbsp;&nbsp;  <sumn>4a</sumn> For friendly salutations, don't include <tp>o</tp>.</sumt><br>
  <sumn>5</sumn> <sumt>To get someone's attention and issue a command in the samen sentence, say, "<tp>jan</tp> (person's name) <tp>o</tp> (verb)."</sumt>

<p><suml>Lesson 10</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>olin, seme, sin, supa, suwi</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>For questions that can't be answered with "yes" or "no," write the sentence like normal and then replace the word in question with <tp>seme</tp>.</sumt><br>
  <sumn>2</sumn> <sumt><tp>seme</tp> can also be used as an adjective to make other question words.</sumt>

<p><suml>Lesson 11</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>a, awen, mama, mije, meli, mu, nimi, o, pona, toki</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt><tp>pi</tp> separates a noun from a modifying noun which has an adjective of its own.</sumt><br>
  <sumn>2</sumn> <sumt>Don't use <tp>pi</tp> if the modiying noun doesn't have an adjective of its own.</sumt>

<p><suml>Lesson 12</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>anu, en, kin, lete, mani, pilin, taso</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt><tp>anu</tp> <eq></eq> <eng>or.</eng> <tp>anu</tp> may imply a question. You can ask a yes/no question by adding "<tp>anu seme?</tp>" to the end of the sentence.</sumt><br>
  <sumn>2</sumn> <sumt><tp>en</tp> <eq></eq> <eng>and.</eng> For the vast majority of cases, <tp>en</tp> can be used only in the sentence's subject.</sumt><br>
  <sumn>3</sumn> <sumt>At the beginning of a sentence, <tp>taso</tp> <eq></eq> <eng>but.</eng> Within sentences, <tp>taso</tp> can be an adjective or adverb.</sumt><br>
  <sumn>4</sumn> <sumt><tp>kin</tp> is an intensifier and is used like normal adjectives/adverbs.</sumt><br>
  <sumn>5</sumn> <sumt>To describe the temperature outside, say, "(temperature) <tp>li lon</tp>.</sumt><br>
  <sumn>6</sumn> <sumt>To describe the temperature of specific objects, say, "(object) <tp>li</tp> (temperature) <tp>pilin</tp>.</sumt><br>

<p><suml>Lesson 13</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>jelo, kule, laso, loje, pimeja, sitelen, walo</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>Elementary colors can be strung together to describe more colors.<br>
  <sumn>2</sumn> <sumt>To describe objects with multiple distinct colors, say, "(object) <tp>pi</tp> (color 1) <tp>en</tp> (color 2)."</sumt>

<p><suml>Lesson 14</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>akesi, kala, kasi, moli, soweli, waso, (also review pipi)</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>Organisms fit into one of six very broad categories. Naming the organisms within each category is very ambiguous.</sumt>

<p><suml>Lesson 15</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>ko, kute, linja, luka, lupa, nena, noka, oko, palisa, selo, sijelo, sike, sinpin, uta</sumv> 

<p><suml>Lesson 16</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>nanpa, tu, wan, weka</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>Toki Pona has words for 1, 2, 5, 20, and 100. These words also have a few other meanings besides numbers.<br>
  <sumn>2</sumn> <sumt>Although discouraged, these words may be stacked to make higher numbers.<br>
&nbsp;&nbsp;&nbsp;<sumn>2a</sumn> <sumt>Use <tp>mute</tp> unless specificity is necessary.</sumt><br>
  <sumn>2</sumn> <sumt>For ordinal numbers, say, "(noun) <tp>pi napna</tp> (number)."</sumt> 

<p><suml>Lesson 17</suml><br>
 <sumh>Vocabulary</sumh><br>
  <sumv>la, mun, open, pini, tenpo</sumv><br>
 <sumh>Topics</sumh><br>
  <sumn>1</sumn> <sumt>"<tp>ken la</tp> (rest of sentence)</tp>" <eq></eq> "Maybe (rest of sentence)."</sumt><br>
  <sumn>2</sumn> <sumt>"(time) <tp>la</tp> (rest of sentence)" tells when something occurred.</sumt><br>
  <sumn>3</sumn> <sumt>"(sentence 1) la (sentence 2)" <eq></eq> "If (sentence 1), then (sentence 2)."</sumt><br>
  <sumn>4</sumn> <sumt>For comparatives and superlatives, say, "(thing that is more of something) <tp>li</tp> (adjective) <tp>mute</tp>. (thing that is less) <tp>li</tp> (adjective) <tp>lili</tp>."</sumt>

